package FirstSteps;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // Ranges of primitive data types
        System.out.println("Range of byte is    : (" + Byte.MIN_VALUE + ", " + Byte.MAX_VALUE + ")");
        System.out.println("Range of short is   : (" + Short.MIN_VALUE + ", " + Short.MAX_VALUE + ")");
        System.out.println("Range of int is     : (" + Integer.MIN_VALUE + ", " + Integer.MAX_VALUE + ")");
        System.out.println("Range of long is    : (" + Long.MIN_VALUE + ", " + Long.MAX_VALUE + ")");
        System.out.println("Range of float is   : (" + Float.MIN_VALUE + ", " + Float.MAX_VALUE + ")");
        System.out.println("Range of double is  : (" + Double.MIN_VALUE + ", " + Double.MAX_VALUE + ")");
        System.out.println("Range of char is    : (" + Character.MIN_VALUE + ", " + Character.MAX_VALUE + ")");
        /*
        Boolean stores values like true or false
         */

    }
}

